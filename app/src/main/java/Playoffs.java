public class Playoffs {
    int hostImg;
    String hostName;
    String hostScore;
    int guestImg;
    String guestName;
    String guestScore;
    public Playoffs(int hostImg, String hostName, String hostScore, int guestImg, String guestName, String guestScore){
        this.hostImg = hostImg;
        this.hostName = hostName;
        this.hostScore = hostScore;
        this.guestImg = guestImg;
        this.guestName = guestName;
        this.guestScore = guestScore;
    }
}
